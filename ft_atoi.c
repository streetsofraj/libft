#include "libft.h"

int		ft_atoi(const char *str)
{
	long s;
	long nb;

	s = 1;
	nb = 0;
	while (*str == ' ' || *str == '\n' || *str == '\r'
			|| *str == '\v' || *str == '\t'
			|| *str == '\f')
		str++;
	if (*str == '-' || *str == '+')
	{
		if (*str == '-')
			s = -1;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		nb = (*str - '0') + nb * 10;
		str++;
	}
	return (s * nb);
}
